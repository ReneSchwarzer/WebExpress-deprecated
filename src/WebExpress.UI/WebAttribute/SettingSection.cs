namespace WebExpress.UI.WebAttribute
{
    /// <summary>
    /// Definition of keys for the identification of sections in Wen pages, which can be occupied by components.
    /// </summary>
    public enum SettingSection
    {
        /// <summary>
        /// Returns the preference section.
        /// </summary>
        Preferences,

        /// <summary>
        /// Returns the primary section.
        /// </summary>
        Primary,

        /// <summary>
        /// Returns the secondary section.
        /// </summary>
        Secondary
    }
}
