namespace WebExpress.WebApp.Attribute
{
    /// <summary>
    /// Definition von Schlüsseln zur Identifikation von Sektionen in Wen-Seiten, welche durch Komponenten besezt werden können.
    /// </summary>
    public enum SettingSection
    {
        /// <summary>
        /// Liefert oder setzt den Preferences-Bereich
        /// </summary>
        Preferences,

        /// <summary>
        /// Liefert oder setzt den Primary-Bereich
        /// </summary>
        Primary,

        /// <summary>
        /// Liefert oder setzt den Secondary-Bereich
        /// </summary>
        Secondary
    }
}
